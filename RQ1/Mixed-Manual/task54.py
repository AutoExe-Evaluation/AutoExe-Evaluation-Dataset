def minSubArraySum(nums):
    assert len(nums) > 0  # PRE
    max_sum = 0
    s = 0
    for num in nums:
        s += -num
        if (s < 0):
            s = 0
        max_sum = max(s, max_sum)
    if max_sum == 0:
        max_sum = max(-i for i in nums)
    min_sum = -max_sum
    assert min_sum <= sum(nums)  # POST
